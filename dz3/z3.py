def _sbox_lookup(i, inp):
    assert len(inp_str := str(bin(inp))[2:].zfill(6)) == 6
    row, col = map(partial(int, base=2), [inp_str[0] + inp_str[-1], inp_str[1:5]])
    return SBOX[i][row][col]


def _in(j, bcj: int, ccj: int):
    return {bj for bj in range(2 ** 6) if _sbox_lookup(j, bj) ^ _sbox_lookup(j, bj ^ bcj) == ccj}


def _test(i, e=None, es=None, cc=None):
    if not all([e, es, cc]):
        e, es, cc = E[i - 1], Es[i - 1], Cc[i - 1]
    return [Bi ^ e for Bi in _in(i, e ^ es, cc)]


def main():
    for i in range(1, 3):
        print(
            f"Test {i}: ",
            *sorted(list(map(
                lambda x: bin(x)[2:].zfill(6),
                _test(i)
            )))
        )


def _test_code():
    assert (
            sorted(_test(1, 0b000001, 0b110101, 0b1101)) ==
            [0b000111, 0b010001, 0b010111, 0b011101, 0b100011, 0b100101, 0b101001, 0b110011]
    )


